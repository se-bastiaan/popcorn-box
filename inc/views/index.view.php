<h1>Welcome to Popcorn Box</h1>
<p>
	Popcorn Box is a easy to use web application to display tv shows and movies from SickBeard and CouchPotato in a user friendly environment. Select a show, an episode and download or watch it instantly. The same thing for the movies that have been downloaded (and haven't been deleted yet).
</p>
<p>
	An episode of your favorite TV show that isn't available? Then just let Sickbeard search for it using the 'search for episode' button. If it finds the show, it will be downloaded and the progress will be visible on the 'Downloads' page.
</p>
<p>
	This application works best in Google Chrome. Watching video's could be more difficult in other webbrowsers.
</p>
